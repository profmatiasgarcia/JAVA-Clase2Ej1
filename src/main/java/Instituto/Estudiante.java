public class Estudiante extends Persona {

    private int legajo;

    private String carrera;

    public Estudiante() {
    }

    public Estudiante(int legajo, String carrera) {
        this.legajo = legajo;
        this.carrera = carrera;
    }

    public Estudiante(int legajo, String carrera, String nombre, int edad, String apellido) {
        super(nombre, edad, apellido);
        this.legajo = legajo;
        this.carrera = carrera;
    }

    public int getLegajo() {
        return legajo;
    }

    public void setLegajo(int legajo) {
        this.legajo = legajo;
    }

    public String getCarrera() {
        return carrera;
    }

    public void setCarrera(String carrera) {
        this.carrera = carrera;
    }

    @Override
    public String toString() {
        return "Estudiante{" + super.toString() + " legajo=" + legajo + ", carrera=" + carrera + '}';
    }


}
